
package tamagotchi;


public class Food 
{
    private String name;
    private int value;
    
    
    public Food()
    {

    }


    public String getName() {
        return name;
    }


    public int getValue() {
        return value;
    }


    public void setName(String name) {
        this.name = name;
    }


    public void setValue(int value) {
        this.value = value;
    }
    
}
